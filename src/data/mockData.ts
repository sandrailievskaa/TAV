export interface Flight {
  id: string;
  flightNumber: string;
  airline: string;
  origin: string;
  destination: string;
  departure: string;
  arrival: string;
  gate: string;
  terminal: string;
  aircraft: string;
  status: 'on-time' | 'delayed' | 'cancelled' | 'boarding' | 'landed';
  passengers: number;
}

export interface Passenger {
  id: string;
  name: string;
  email: string;
  phone: string;
  passport: string;
  nationality: string;
  flightNumber: string;
  bookingRef: string;
  seatNumber: string;
  class: 'Economy' | 'Business' | 'First';
  status: 'checked-in' | 'boarded' | 'pending';
  checkInTime?: string;
  boardingTime?: string;
}

export interface Staff {
  id: string;
  employeeId: string;
  name: string;
  email: string;
  phone: string;
  department: string;
  position: string;
  shift: 'Morning' | 'Afternoon' | 'Night';
  status: 'active' | 'on-leave' | 'off-duty';
  hireDate: string;
  supervisor: string;
  badgeExpiry?: string;
  certificationExpiry?: string;
}

export interface Vehicle {
  id: string;
  vehicleId: string;
  type: string;
  licensePlate: string;
  assignedTo: string;
  location: string;
  fuelLevel: number;
  status: 'active' | 'maintenance' | 'idle';
  lastService: string;
  nextService: string;
  inspectionExpiry?: string;
  insuranceExpiry?: string;
}

export interface Activity {
  id: string;
  type: 'flight' | 'passenger' | 'staff' | 'vehicle' | 'system';
  message: string;
  timestamp: string;
  user: string;
}

